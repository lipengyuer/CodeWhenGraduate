function nn = nnff(nn, x, y)
%NNFF performs a feedforward pass（这段注释挺赞的）
% nn = nnff(nn, x, y) returns an neural network structure with updated
% layer activations, error and loss (nn.a, nn.e and nn.L)

    n = nn.n;  %n中存储的是层的个数
    m = size(x, 1); %m中存储的是训练集的个数
    
    x = [ones(m,1) x];  % x 会变成有1的新的矩阵
    nn.a{1} = x; %这个存储的前向传播的过程中的中间结果

    %feedforward pass
    for i = 2 : n-1
        switch nn.activation_function 
            case 'sigm'
                % Calculate the unit's outputs (including the bias term)
                nn.a{i} = sigm(nn.a{i - 1} * nn.W{i - 1}');
            case 'tanh_opt'
                nn.a{i} = tanh_opt(nn.a{i - 1} * nn.W{i - 1}');
        end
        
        %dropout  判断是否进行dropout
        if(nn.dropoutFraction > 0)
            if(nn.testing)   %如果nn.testing为真，那么将矩阵中的值按照特定比例下降
                nn.a{i} = nn.a{i}.*(1 - nn.dropoutFraction);
            else  %如果nn.testing不为真，
                nn.dropOutMask{i} = (rand(size(nn.a{i}))>nn.dropoutFraction);  %在这里会生成一个布尔矩阵
                nn.a{i} = nn.a{i}.*nn.dropOutMask{i}; %这里的做法是将很多地方直接变为0，其它的位置保持不变
            end
        end
        
        %calculate running exponential activations for use with sparsity
        % nn.p{i}的初值是是一个向量而且全是0,所以，nn.p{1}在本质上是被稀疏化了的值
        if(nn.nonSparsityPenalty>0)
            nn.p{i} = 0.99 * nn.p{i} + 0.01 * mean(nn.a{i}, 1);
        end
        
        %Add the bias term
        nn.a{i} = [ones(m,1) nn.a{i}];
    end
    switch nn.output 
        case 'sigm'
            nn.a{n} = sigm(nn.a{n - 1} * nn.W{n - 1}');
        case 'linear'
            nn.a{n} = nn.a{n - 1} * nn.W{n - 1}';
        case 'softmax'
            nn.a{n} = nn.a{n - 1} * nn.W{n - 1}';
            nn.a{n} = exp(bsxfun(@minus, nn.a{n}, max(nn.a{n},[],2)));
            nn.a{n} = bsxfun(@rdivide, nn.a{n}, sum(nn.a{n}, 2)); 
    end

    %error and loss
    nn.e = y - nn.a{n};
    
    switch nn.output
        case {'sigm', 'linear'}
            nn.L = 1/2 * sum(sum(nn.e .^ 2)) / m; 
        case 'softmax'
            nn.L = -sum(sum(y .* log(nn.a{n}))) / m;
    end
end
