% 3x + 1 问题
% 从任意一个正整数开始，重复对其进行下面的操作：如果这个数是偶数，把它除以 2 ；如果这个数是奇数，则把它扩大到原来的 3 倍后再加 1 。你会发现，序列最终总会变成 4, 2, 1, 4, 2, 1, … 的循环。
x (1)= fix(rand*1000);
for m = 1:100
    if(rem(x(m),2) == 0)
       x(m+1) = x(m)/2;
    elseif(rem(x(m),2) == 1)
       x(m+1) = 3*x(m) + 1;
    end
end


    
        
